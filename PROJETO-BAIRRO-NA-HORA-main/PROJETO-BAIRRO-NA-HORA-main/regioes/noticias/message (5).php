                <p>
                    Histórias vividas por personagens que moram no Setor Habitacional Sol Nascente, no Distrito Federal, compõem o documentário “O sol nasceu para todos”, que tem lançamento às 19h desta quinta-feira (14), no teatro Newton Rossi, no Sesc de Ceilândia. O projeto foi executado por dez pessoas da comunidade e demorou 14 meses para ficar pronto, entre pesquisa, filmagem e finalização.
                </p>
        
                <p>
                    O longa traz depoimentos de lideranças comunitárias, artistas, esportistas e pessoas comuns da localidade falando do dia a dia, das lutas e conquistas, como protagonistas da história. A ideia dos realizadores foi dar ao morador “as rédeas da própria vida”.

“A gente conseguiu mostrar um Sol Nascente ‘positivo’, mostrou que tudo o que é falado da periferia é falado por quem está de fora. Nós temos problemas, sim, como em qualquer periferia, em outras cidades, mas temos histórias de pessoas que encontraram esperança, realizaram sonhos”, afirma o jornalista Davidson Pereira, produtor do filme.

“No momento em que conseguimos falar de nós mesmos, podemos transformar o nosso espaço. A gente quis mostrar para as pessoas que não conhecem as periferias, que elas são bem-vindas; e ao morador do Sol Nascente, que há histórias boas aqui, que é possível ter orgulho e mudar a própria realidade, mesmo sem apoio do poder público.”

O documentário tem depoimentos com “começo, meio e fim”, concluindo histórias de alguns personagens, e inserções de moradores que “ajudam a compreender o contexto” e a traçar um panorama do que acontece na área, como diz o diretor do filme, Alan Mano K.
                </p>
        
                <p>
                    “A gente procurou construir uma narrativa que conseguisse falar não só do ponto histórico. A gente não tem a pretensão de contar uma história oficial, mas apenas mostrar alguns aspectos, como a parte da cultura, o trabalho social, os empreendedores, alguns [personagens] que trabalham como lideranças.”
                </p>
        
                <p>
                    O Sol Nascente é considerado a maior favela da América Latina. O projeto urbanístico é dividido em três trechos, que juntos somam 940 hectares (o equivalente  a 940 campos de futebol). A área apresenta problemas de saneamento básico, acúmulo de lixo, buracos nas ruas, barracos em condições precárias e altos índices de criminalidade.

Um estudo feito por amostragem pelo Instituto Brasileiro de Geografia e Estatística (IBGE) apontou que a área tinha 56,5 mil moradores em 2010. Na época, o Sol Nascente estava abaixo somente da favela da Rocinha, no Rio de Janeiro, em número de habitantes.
                </p>
        
                <p>
                    Parceria
                    O documentário é uma realização do Programa Jovem de Expressão e da Rede Urbana de Ações Socioculturais (Ruas), em parceria com a Embaixada da Suíça e a Associação Suíça de Futebol. A ideia surgiu em 2014, quando a equipe esportiva do país europeu esteve em Brasília para o jogo contra a Equador, pela Copa do Mundo.
                    
                    “A Embaixada da Suíça entrou em contato com o Expressão e o Ruas por meio do UNODC, que é o Escritório das Nações Unidas sobre Drogas e Crimes, parceiro do projeto, e disse que queria financiar um projeto social por aqui, meio como um legado da Copa”, afirma Davidson Pereira.
                </p>
